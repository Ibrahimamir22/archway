/**
 * Default mapping of service category slugs to icon names
 */
export const categoryToIconMap: Record<string, string> = {
  residential: 'home',
  commercial: 'building',
  office: 'briefcase',
  visualization: 'image',
  consultation: 'message-circle',
  design: 'pen-tool',
  renovation: 'tool',
  sustainable: 'leaf',
  default: 'grid'
};

interface ServiceIconProps {
  iconName?: string;
  categorySlug?: string;
  className?: string;
}

/**
 * Renders the appropriate service icon based on name or category
 */
const ServiceIcon: React.FC<ServiceIconProps> = ({ 
  iconName, 
  categorySlug,
  className = 'w-6 h-6 text-brand-blue-light' 
}) => {
  // Determine which icon to use
  let iconToUse = 'grid'; // Default icon
  
  if (iconName && serviceIconMap[iconName]) {
    iconToUse = iconName;
  } else if (categorySlug && categoryToIconMap[categorySlug]) {
    iconToUse = categoryToIconMap[categorySlug];
  }
  
  return (
    <svg 
      className={className} 
      fill="none" 
      stroke="currentColor" 
      viewBox="0 0 24 24" 
      xmlns="http://www.w3.org/2000/svg"
    >
      {serviceIconMap[iconToUse] || serviceIconMap.grid}
    </svg>
  );
};

export default ServiceIcon; 